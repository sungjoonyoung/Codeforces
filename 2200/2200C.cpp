using namespace std;
void solve(void) {
    int N;cin>>N;
    string str;cin>>str;
    vector<char> v;
    for(int i=0;i<N;i++){
        char c=str[i];
        if(v.empty() or v.back()!=c)v.push_back(c);
        else v.pop_back();
    }
    if(v.empty())cout<<"YES\n";
    else cout<<"NO\n";
}
int main(void) {
    ios::sync_with_stdio(0);
    cin.tie(0);
    int T=1;
    cin>>T;
    while(T--)solve();
}